public class SumaClasse {


    // Mètode sumar, retorna el valor de la suma de num1 i num2
    public int sumar(int num1, int num2) {
        return num1 + num2;
    }


    // Mètode getNum, que ens retorna el valor del número 1
    public int getNum1(int num1) {
        return num1;
    }


    // Metode valor absolut, retorna la suma dels valors absoluts de num1 i num2
    public int sumaValorAbsolut(int num1, int num2) {
        return Math.abs(num1) + Math.abs(num2);
    }

}